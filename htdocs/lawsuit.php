<h1>Fraudulent Lawsuit Against Mainlands 7</h1>
<p>
	On April 9, 2012, a $1 million lawsuit was served upon the homeowners of Mainlands 7 demanding the resignation of two Board members who had been investigating the HOA finances.
</p>
<p>
	It was later proved that the lawsuit itself was a forgery using counterfeit seals of the Clerk of Courts.  In addition, the author impersonated an innocent homeowner and their business, potentially causing damage to their reputation.
</p>
<p>
	If you have information about this crime, please contact the Broward Sheriff's Office Tamarac Criminal Investigations Unit at 954-720-2231.  If you prefer to remain anonymous, you may submit tips through the <a href="https://www.tipsubmit.com/WebTips.aspx?AgencyID=157">Broward County Crime Stoppers</a> web site.  In both cases, reference case number TM12-05-01281.
</p>
<p>
	The latest information about this crime can be found on the <a href="http://groups.yahoo.com/group/mainlands7">Mainlands 7 Yahoo! group</a>.
</p>

